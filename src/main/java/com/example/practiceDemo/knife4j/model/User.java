package com.example.practiceDemo.knife4j.model;

import io.swagger.annotations.ApiModelProperty;

public class User {
    @ApiModelProperty("id")
    private String id;
    @ApiModelProperty("名字")
    private String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "User{" +
                "id='" + id + '\'' +
                ", name='" + name + '\'' +
                '}';
    }
}
